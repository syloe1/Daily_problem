#include <bits/stdc++.h>
using namespace std; 
int main() {
    int q;cin>>q; 
    stack<int> st;
    for(int i = 0; i < q; i++) {
        int c;cin>>c;
        if(c==1) {
            int x;cin>>x; st.push(x);
        }
        else {
            if(!st.empty()) {
                cout<<st.top()<<'\n';
                st.pop();
            } else {
                cout<<0<<'\n';
            }
        }
    }
    return 0;
}
